const Sidebar = ({ collapsed, onToggle, activeItem, onNavClick, userName, userRole }) => {
  const { signOut, role } = useAuth();
  const navigate = useNavigate();

  const handleSignOut = async () => {
    await signOut();
    navigate("/");
  };

  // Filter nav items to only those accessible by the current user's role
  const visibleItems = navItems.filter(
    (item) => item.roles.includes(role)
  );

  return (
    <aside className={`sidebar ${collapsed ? "collapsed" : ""}`}>
      {/* Header */}
      <div className="sidebar-header" style={collapsed ? { justifyContent: "center" } : undefined}>
        {!collapsed && <img src={logoImg} className="sidebar-logo" alt="OTTO Logo" />}
        {!collapsed && <span className="sidebar-brand">Otto Shoes</span>}
        <button className="sidebar-toggle" onClick={onToggle} aria-label="Toggle sidebar" style={collapsed ? { margin: 0 } : undefined}>
          <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            {collapsed ? (
              <polyline points="9 18 15 12 9 6" />
            ) : (
              <polyline points="15 18 9 12 15 6" />
            )}
          </svg>
        </button>
      </div>

      {/* Navigation */}
      <nav className="sidebar-nav">
        {visibleItems.map((item) => (
          <button
            key={item.id}
            className={`sidebar-link ${activeItem === item.id ? "active" : ""}`}
            onClick={() => onNavClick(item.id)}
            title={collapsed ? item.label : undefined}
          >
            <span className="sidebar-link-icon">{item.icon}</span>
            <span className="sidebar-link-text">{item.label}</span>
          </button>
        ))}
      </nav>

      {/* User / Sign Out */}
      <div className="sidebar-user">
        <div className="sidebar-user-avatar">
          {userName ? userName.split(" ").map(n => n[0]).join("").substring(0, 2).toUpperCase() : "IC"}
        </div>
        <div className="sidebar-user-info">
          <div className="sidebar-user-name">{userName}</div>
          <div className="sidebar-user-role">{userRole}</div>
        </div>
        {!collapsed && (
          <button className="sidebar-logout-btn" onClick={handleSignOut} title="Sign Out">
            <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
              <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4" />
              <polyline points="16 17 21 12 16 7" />
              <line x1="21" y1="12" x2="9" y2="12" />
            </svg>
          </button>
        )}
      </div>
    </aside>
  );
};

export default Sidebar;
